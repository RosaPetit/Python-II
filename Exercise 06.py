'''Programa 5 (uso avanzado del for)'''

a=range(10,100,2)
# a=rango(de diez end diez, que llegue hasta 100, de dos en dos )

suma=0
#"suma" es para definir desde donde empezaremos a sumar  

for i in a:
	suma=suma+i
#Luego a "suma" le sumas i que es del ciclo for que contiene el rango
	print(i,suma)

print(suma)

	

